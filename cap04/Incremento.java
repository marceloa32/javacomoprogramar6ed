public class Incremento{

	public static void main(String[] args){
	
		int c;
		
		c = 5;
		System.out.println(c);
		System.out.println(c++);
		System.out.println(c);
		
		System.out.println();
		
		c = 5;
		System.out.println(c);
		System.out.println(++c);
		System.out.println(c);		
	}
}
